CREATE TABLE players (
  ilkid varchar(20),
  firstname varchar(20) NOT NULL,
  lastname varchar(20) NOT NULL,
  position char(1) NOT NULL,
  firstseason int,
  lastseason int,
  h_feet int,
  h_inches int,
  weight int,
  college varchar(20) NOT NULL,
  birthdate datetime,
  PRIMARY KEY (ilkid),
  CHECK (firstseason >= 0),
  CHECK (lastseason >= 0),
  CHECK (h_feet >= 0),
  CHECK (weight >= 0),
  CHECK (birthdate  >= 0)
);

CREATE TABLE teams (
  team CHAR(3) NOT NULL,
 	location VARCHAR(20) NOT NULL,
 	name VARCHAR(20),
 	leag CHAR(1) NOT NULL,
 	PRIMARY KEY (team, leag)
);
