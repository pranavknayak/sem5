CREATE TABLE coaches_career (
  coachid varchar(20),
  firstname varchar(20) NOT NULL,
  lastname varchar(20) NOT NULL,
  season_win int,
  season_loss int,
  playoff_win int,
  playoff_loss int,
  PRIMARY KEY (coachid),
  CHECK (season_win >= 0),
  CHECK (season_loss >= 0),
  CHECK (playoff_win >= 0),
  CHECK (playoff_loss >=0)
);

CREATE TABLE coaches_season (
  coachid varchar(20),
  year int,
  yr_order int,
  firstname varchar(20) NOT NULL,
  lastname varchar(20) NOT NULL,
  season_win int,
  season_loss int,
  playoff_win int,
  playoff_loss int,
  team char(3),
  CHECK (season_win >= 0),
  CHECK (season_loss >= 0),
  CHECK (playoff_win >= 0),
  CHECK (playoff_loss >=0),
  CHECK (year >= 1946),
  CHECK (yr_order >= 1)
);

